function soma (item1, item2){
    return item1 + item2;
}

function subtraçao (item1, item2) {
    return item1 - item2;
}

function divisao (item1, item2) {
    return item1 / item2;
}

function multiplicaçao (item1, item2) {
    return item1 * item2;
}
console.log(soma(5, 6))
console.log(subtraçao(10, 6))
console.log(divisao(20, 2))
console.log(multiplicaçao(3, 3))